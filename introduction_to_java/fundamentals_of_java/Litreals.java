public class Litreals {
void main()
{
    //litrels are nothing but content inside variable 
    byte b = 2; //here 2 is byte literal and b is variable
    float f = 12.2f; //12.2f is float variable
    long l = 12345678l; //long literal
    boolean bul = true; //boolean literal
    int bin = 0b1111; //binary literal
    int hex = 0xA1; //hex literal
    long num = 1_2_3_4; //we can use underscore to divide large number
    String name = "abhinav";//string literal

     System.out.println(name);
    System.out.println(l);
    System.out.println(f);
    System.out.println(b);
    System.out.println(bul);
    System.out.println(bin);
    System.out.println(hex);
    System.out.println(num);
}
}
